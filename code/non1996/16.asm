DATA SEGMENT
	NUM DW 0FFFFH
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START:
	MOV AX, DATA
	MOV DS, AX

	MOV AX, NUM
	MOV DX, 0
	MOV CL, 2
COUNT:
	MOV BL, AL		;
	AND BL, 03H		;每次取AX的最低两位，如果等于11b, 计数器dx加1
	CMP BL, 03H		;	
	JNZ NOTEQUAL
	INC DX	
NOTEQUAL:
	SHL AX, CL		;ax每次右移两位，当ax等于0时跳出循环
	CMP AX, 0
	JNZ COUNT
	
	MOV AH, 02H
	ADD DL, '0'		;将dl转化为ascii码并输出
	INT 21H

	MOV AH, 04CH
	INT 21H
CODE ENDS
	END START