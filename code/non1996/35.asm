DATA SEGMENT
	SCORES DW 76, 69, 84, 90, 73, 88, 99, 63, 100, 80
	S6 DW 0
	S7 DW 0
	S8 DW 0
	S9 DW 0
	S10 DW 0	
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START:
	MOV AX, DATA
	MOV DS, AX

	MOV SI, OFFSET SCORES
	MOV CX, 10      
COUNT:
	CMP [SI], 70
	JB SIX
	CMP [SI], 80
	JB SEVEN
	CMP [SI], 90
	JB EIGHT
	CMP [SI], 100
	JB NINE
	JMP TEN
SIX:
	INC S6
	JMP NEXT
SEVEN:
	INC S7
	JMP NEXT
EIGHT:
	INC S8
	JMP NEXT
NINE:
	INC S9
	JMP NEXT
TEN:	
	INC S10
NEXT:	
	ADD SI, 2
LOOP COUNT	

FINISH:
	MOV AH, 4CH
	INT 21H
CODE ENDS
	END START