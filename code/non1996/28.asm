DATA SEGMENT
	A DW 1
	B DW 0
	C DW 3
	D DW 0
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START:
	MOV AX, DATA
	MOV DS, AX
      
	CMP A, 0
	JZ CLEAR
	CMP B, 0
	JZ CLEAR
	CMP C, 0
	JZ CLEAR
	
	MOV AX, A
	ADD AX, B
	ADD AX, C
	MOV D, AX
	JMP FINISH

CLEAR:
	MOV A, 0
	MOV B, 0
	MOV C, 0	

FINISH:
	MOV AH, 4CH
	INT 21H
CODE ENDS
	END START