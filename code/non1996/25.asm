DATA SEGMENT
	HEXNUM DW 2A49H
	MEM DB 4 DUP(0)
	MEM_END DB 0		;标记数组结尾
DATA ENDS 

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START:
	MOV AX, DATA
	MOV DS, AX

	MOV AX,WORD PTR HEXNUM

	MOV CL, 4
	MOV SI, OFFSET MEM
	MOV DX, OFFSET MEM_END

;----------------------------------------------每次将第四位转换为ascii码
TRANSLATE:
	MOV BL, AL
	AND BL, 0FH	
	
	CMP BL, 9
	JA ALPHA
	ADD BL, '0'
	JMP SAVE
ALPHA:          
    SUB BL, 10
	ADD BL, 'A'
SAVE:
	MOV [SI], BL
	
	SHR AX, CL
	INC SI
	CMP SI, DX
	JNZ TRANSLATE	

	MOV AH, 4CH
	INT 21H
CODE ENDS
	END START