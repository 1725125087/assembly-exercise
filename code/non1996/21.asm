DATA SEGMENT 
	STRING DB 50 DUP('A'), 50 DUP('1')
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START:
	MOV AX, DATA
	MOV DS, AX
	
	
;---------------------------------遍历数组寻找数字
	MOV BX, OFFSET STRING
	MOV CX, 100	
FIND_DIGIT:			
	CMP [BX], '0'
	JB HAS_DIGIT
	CMP [BX], '9'
	JA NOT_DIGIT
	JMP HAS_DIGIT
NOT_DIGIT:
	INC BX
LOOP FIND_DIGIT
   
HASN_DIGIT:    
    MOV CL, 0
	JMP FINISH		

HAS_DIGIT:
    MOV CL, 0
	OR CL, 10H	

FINISH:	
	MOV AH, 4CH
	INT 21H
CODE ENDS
	END START