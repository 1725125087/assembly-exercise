TITLE seagullbird_t21
.MODEL SMALL
.DATA
	STRING DB 100 DUP (?)
.CODE
MAIN PROC FAR
	ASSUME CS:_TEXT, DS:_DATA
	PUSH DS
	XOR AX, AX
	PUSH AX
	MOV AX, @DATA
	MOV DS, AX

	XOR CL, CL
	XOR BX, BX
	FOR:
		CMP BX, 99
		JG FINAL
		CMP BYTE PTR [BX], 30H
		JL PASS
		CMP BYTE PTR [BX], 39H
		JG PASS
		OR CL, 00010000B			; SET 5TH TO 1
		JMP FINAL
		PASS:
			INC BX
		JMP FOR

	FINAL:
		RET
MAIN ENDP
	END MAIN