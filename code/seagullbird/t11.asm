TITLE seagullbird_t11

.MODEL SMALL
.DATA
	NUMBER DW 1234H
.CODE
MAIN PROC FAR
	ASSUME CS:_TEXT, DS:_DATA
	PUSH DS
	XOR AX, AX
	PUSH AX
	MOV AX, @DATA
	MOV DS, AX

	MOV	AX, NUMBER
	MOV CL, 4
	MOV BX, AX
	AND AX, 0FH
	ROR BX, CL
	MOV SI, BX
	AND BX, 0FH
	ROR SI, CL
	MOV DX, SI
	AND SI, 0FH
	ROR DX, CL
	AND DX, 0FH
	MOV CX, SI
	RET
MAIN ENDP
	END MAIN