TITLE seagullbird_t13
.MODEL SMALL
.DATA
	MESSAGE DB 'PLEASE INPUT A NUMBER TO RING: $'
.CODE
MAIN PROC FAR
	ASSUME CS:_TEXT, DS:_DATA
	PUSH DS
	XOR AX, AX
	PUSH AX
	MOV AX, @DATA
	MOV DS, AX
	PRINT:
		MOV AH, 09
		LEA DX, MESSAGE
		INT 21H

	MOV AH, 01H
	INT 21H
	CMP AL, 30H
	JL PRINT
	CMP AL, 39H
	JG PRINT

	SUB AL, 30H
	MOV CL, AL
	MOV DL, 07H
	MOV AH, 02H
	LP:
		INT 21H
		LOOP LP

	RET
MAIN ENDP
	END MAIN